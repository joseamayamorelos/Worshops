package Publico;

public class Util {
    

    public int sumar(int a, int b) {
        return a + b;
    }

    public int rest(int a, int b) {
        return a - b;
    }


    public int mult(int a, int b) {
        return a * b;
    }


    public int divi(int a, int b) {
        return a / b;
    }
    

}
